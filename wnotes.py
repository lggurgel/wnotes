    with open(FILENAME, "ab+") as outfile:
        name = input("Digite seu nome\n")
        note = input("\n\nDigite sua mensagem aos noivos\n")

        # if name == "ana" and note == "lucas":
        #     print_notes()

        input_note = {
            "name": name,
            "note": note,
        }

        pickle.dump(input_note, outfile)

    return input_note


